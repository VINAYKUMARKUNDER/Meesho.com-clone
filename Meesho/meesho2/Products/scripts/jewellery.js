function displayData(jewelArr){

    document.querySelector("#container").innerHTML = "";  

    jewelArr.map(function (elem) {

        var maindiv = document.createElement("div")
        maindiv.setAttribute("id","maindiv");
        maindiv.addEventListener("click",function (){
          detailsFun(elem);
      });

        var img = document.createElement("img")
        img.setAttribute("id","menImg")
        img.setAttribute("src",elem.image_url);

        var name= document.createElement("h3")
        name.setAttribute("id","name")
        name.innerText = elem.name;

        var priceDiv = document.createElement("div")
        priceDiv.setAttribute("id","priceDiv")
       
        var price= document.createElement("p")
        price.setAttribute("id","price")
        price.innerText = `₹ ${elem.price}`;

        var strikedoffprice = document.createElement("p")
        strikedoffprice.innerText= elem.strikedoffprice;
        strikedoffprice.setAttribute("id","strikepri")
        strikedoffprice.style.textDecoration = "line-through";

        var discount = document.createElement("p")
        discount.setAttribute("id","discount")
        discount.innerText= elem.discount

        var delivery = document.createElement("div")
        delivery.setAttribute("id","delivery")
        delivery.innerText =" Free Delivery"

        var ratdiv = document.createElement("div")
        ratdiv.setAttribute("id", "ratdiv")

        var rating = document.createElement("div")
        rating.setAttribute("id","rating")
        rating.innerText = `${elem.rating} ✰`
      
        var rev = document.createElement("p")
        rev.setAttribute("id","review")
        rev.innerText = elem.review
           
        priceDiv.append(price,strikedoffprice)  
        ratdiv.append(rating,rev)      

        maindiv.append(img,name,priceDiv,discount,delivery,ratdiv)

        document.querySelector("#container").append(maindiv);    
    });    
}

function detailsFun(elem){    
    elem.qty = 1;
    // console.log(elem)
    // add quant as 1 by default ,while adding to cart
    detailsArr.push(elem);
    console.log(detailsArr)
    localStorage.setItem("details", JSON.stringify(detailsArr))
    window.location.href = "details.html"
}







    